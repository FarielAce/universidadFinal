    private void jBEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarActionPerformed
        Object[] opciones = {"SI", "NO"};

        int opcion = JOptionPane.showOptionDialog(null,
                "多Esta seguro de eliminar la materia?",
                "Confirmacion",
                JOptionPane.DEFAULT_OPTION,
                JOptionPane.YES_NO_OPTION,
                null, opciones, opciones[1]);

        if (opcion == JOptionPane.YES_OPTION) {
            Principal.controlMat.eliminarMateria(busco.getId());
            limpiarJt();
            blkEliminar(false);
            blkEditar(false);
            blkNuevo(true);

        } else if (opcion == JOptionPane.NO_OPTION) {
            limpiarJt();
            blkEliminar(false);
            blkEditar(false);
            blkNuevo(true);

        }
    }//GEN-LAST:event_jBEliminarActionPerformed

    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        if (jTNombre.getText().isEmpty() || jTAnio.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "todos los datos son obligatorios");
        } else {
            Object[] opciones = {"SI", "NO", "CANCELAR"};

            int opcion = JOptionPane.showOptionDialog(null,
                    "多Todos los datos son Correctos?",
                    "Confirmacion",
                    JOptionPane.DEFAULT_OPTION,
                    JOptionPane.YES_NO_CANCEL_OPTION,
                    null, opciones, opciones[2]);
            //jRBEstado.setSelected(true);
            jRBEstado.setEnabled(false);

            if (opcion == JOptionPane.YES_OPTION) {
                if (materiaEditada) {
                    int idMat = Integer.parseInt(jTCodigo.getText());
                    int anio = Integer.parseInt(jTAnio.getText());
                    String nombre = jTNombre.getText();
                    boolean estado = jRBEstado.isSelected();
                    Materia nuevo = new Materia(idMat, nombre, anio, estado);
                    Principal.controlMat.modificarMateria(nuevo);
                    limpiarJt();
                    jTCodigo.setEnabled(true);
                    bloquear(false);
                    blkGuardar(false);
                    blkEditar(false);
                    blkNuevo(true);
                    salir = false;

                } else {
                    int anio = Integer.parseInt(jTAnio.getText());
                    String nombre = jTNombre.getText();
                    boolean estado = jRBEstado.isSelected();
                    Materia nuevo = new Materia(busco.getId(), nombre, anio, estado);
                    Principal.controlMat.modificarMateria(nuevo);
                    limpiarJt();
                    jTCodigo.setEnabled(true);
                    bloquear(false);
                    blkGuardar(false);

                }
            } else if (opcion == JOptionPane.NO_OPTION) {

            } else if (opcion == JOptionPane.CANCEL_OPTION) {
                limpiarJt();
                bloquear(false);
                blkGuardar(false);
                jTCodigo.setEnabled(true);
                blkNuevo(true);
                blkEditar(false);

            }
        }

    }//GEN-LAST:event_jBGuardarActionPerformed

    private void jBSalirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSalirActionPerformed
        if (salir) {
            Object[] opciones = {"VOLVER", "SALIR"};
            int opcion = JOptionPane.showOptionDialog(null,
                    "多Desea volver a Buscar?",
                    "Confirmacion",
                    JOptionPane.DEFAULT_OPTION,
                    JOptionPane.YES_NO_OPTION,
                    null, opciones, opciones[1]);
            if (opcion == JOptionPane.YES_OPTION) {
                limpiarJt();
                blkEliminar(false);
                blkEditar(false);
                blkNuevo(true);
                blkGuardar(false);
                bloquear(false);
                jBBuscar.setEnabled(true);
                jTCodigo.setEnabled(true);
                salir = false;
            } else {
                dispose();
            }
        } else {
            dispose();

        }
    }//GEN-LAST:event_jBSalirActionPerformed

    private void jBBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarActionPerformed

        if (jTCodigo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No hay id de materia cargado");
        } else {
            int cod = Integer.parseInt(jTCodigo.getText());
            busco = Principal.controlMat.buscarMateria(cod);
            if (busco == null) {
                limpiarJt();
                blkEliminar(false);
                blkEditar(false);
                blkNuevo(true);
            } else {
                jTNombre.setText(busco.getNombre());
                int anio = busco.getAnio();
                jTAnio.setText("" + anio);
                jRBEstado.setSelected(busco.isEstado());
                blkEliminar(true);
                blkEditar(true);
                blkNuevo(true);
            }
        }

        //}
    }//GEN-LAST:event_jBBuscarActionPerformed

    private void jBEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarActionPerformed
        blkNuevo(false);
        blkEliminar(false);
        bloquear(true);
        blkGuardar(true);
        jTCodigo.setEnabled(false);
        materiaEditada = true;
        jRBEstado.setEnabled(true);

    }//GEN-LAST:event_jBEditarActionPerformed

